				<h2 class="alpha">Equivalency & Advising</h2>
                <p class="blog-post-meta">April 21, 2014 by <a href="/">Tyler</a></p>
                <p>For me the most complicated part of the study abroad process so far has been picking the correct classes at UEA and making sure they fit into my degree plan, that they are the correct number of credit hours and that a department chair would sign off on them. The process basically took three to four weeks because you have to meet with several department chairs, depending on the types of classes you take, and then meet with an advisor. I was lucky to have a great advisor since she helped me through most of the process &mdash; I was certainly grateful for her help. Transfer credit is difficult because European schools have a different credit system. All full-time students in the UK take 60 UCU credits per semester. Each class is usually 20 UCU credits so a traditional semester only contains three classes. In my case, for financial aid to be applied to my account and to be considered a full-time American student, I had to meet the full-time requirement for both schools &mdash; 60 UCU credits and 12 credit hours. You can see how this can be troubling with 3 credit hour classes or even a 5 credit hour class with two 3 credit hour classes. My advice for anyone thinking about studying abroad - travel Sophomore or Junior year, meet often with your college's advisor and start the equivalency process as soon as you can. Ask your study abroad office for help if you need someone to speak to an advisor or department chair about credits.</p>
