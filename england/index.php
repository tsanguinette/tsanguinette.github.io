            <h2 class="alpha">And So The Story Begins</h2>
            <p class="blog-post-meta">April 21, 2014 by <a href="/">Tyler</a></p>
            <p>Over the next eight months I have decided to share my study aboard semester with everyone. You will see the preparation and planning before the trip as well as the experience aboard itself.</p>
			<p>I have decided to spend my last semester as an undergrad student studying art history at the University of East Anglia in Norwich, England. Upon return, near Christmas time, I will have completed all the classes for a Bachelor of Fine Arts with an emphasis in Graphic Design and a minor in Art History. However, the school can not issue an official certificate of completion until the following spring semester because international grading and transcript requests do not make the December graduation deadline so my official graduation date is set for Spring of 2015, however, I will not have any classes in the Spring semester of 2015.</p>
            <h3>Why Art History and Why England?</h3>
            <p>With the graphic design program at the University of Missouri &mdash; St. Louis, it only takes two more upper level art history classes on top of the required five to receive a minor in the subject. Having a minor in art history can open positions related to preservation, museums, galleries and cultural influence. I chose UEA (<a class="yellowLink" href="https://www.uea.ac.uk/">University of East Anglia</a>) because UMSL has a great partnership with this school, not to mention its one of the highest rated schools in England. It also has one of the best art history departments since the <a class="yellowLink" href="http://www.scva.ac.uk/">Sainsbury Centre for Visual Arts</a> is located on campus with a full art history only library and collection of world renown art &mdash; how cool is that?</p>
            <h3>London, Right?</h3>
            <p>Wrong. The University of East Anglia is in the small town of Norwich which is located in the territory of Norfolk on the east coast of England, across from Amsterdam and near Cambridge. Norwich is two hours, by train, Northeast of central London and five hours from Paris.<br/><img class="img-responsive" src="images/map.jpg"/></p>
            <h3>Do You Need A Visa?</h3>
            <p>Yes. My studies will be less than 6 months so I am able to obtain a student visitor visa at port of entry. If I wanted to stay longer than 6 months I would have to come back to the states, apply for a longer visa and then head back to England.</p>
            <h3>How Long?</h3>
            <p>I have to officially be in England on September 16-17 but classes do not start until September 22. Classes end on December 12th so my return flight will be on December 14 or 15 (Norwich time) just in time for Christmas.</p>
            <h3>Where Will You Live?</h3>
            <p>I will be staying on campus in a dorm. International students are guaranteed housing on campus so although I have not been assigned a room yet I have a spot reserved.</p>
			<h3>Study Abroad, He Must Have Money.</h3>
            <p>Quite the contrary. All of my funds will come from scholarships, grants, savings and student loans. Every bit of funding/support helps.</p>
            <h3>Will You Work?</h3>
            <p>I will not hold an official job overseas. Doing so requires a student work visa which is very costly. I would not make enough money in the short-term trip to cover the visa itself. However, I still plan to do as much freelance work as I can during my studies much like I currently do today.</p>
