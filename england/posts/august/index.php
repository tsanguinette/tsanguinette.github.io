	                  <p class="blog-post-meta">August 19, 2014 by <a href="/">Tyler</a></p>
	                  <p>Since I have a three hour layover in Amsterdam before I connect to Norwich I’ve decided to purchase an airport lounge pass at the Menzies Executive lounge for $30. The Menzies lounge is a quite, reserved, area airside that includes breakfast, drinks, WIFI and comfy seating. I found that it was cheaper to buy through Expedia.</p>
					  <p>It is also worth mentioning that EU airports are divided into Schengen and Non-Schengen terminals or known in Amsterdam as Piers. Schengen flights allow you to travel within designated countries without going through passport control multiple times. Non-Schengen are flights not listed in the Schengen agreement, namely the U.S. and other non EU partners. A complete list of Schengen can be found on the <a href="http://travel.state.gov/content/passports/english/go/schengen-fact-sheet.html">State Travel</a> website</p>
