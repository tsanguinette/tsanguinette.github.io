					            	<h2 class="alpha">Bagpipes, Kilts and Sheep</h2>
					                   <p class="blog-post-meta">December 04, 2014 by <a href="/">Tyler</a></p>
									   <p><img class="img-responsive" src="../../images/scotland.jpg"></p>
									   <p>I recently spent 3 days in Edinburgh — the capital of Scotland. It was interesting to see the cultural differences between the English and the Scottish but between you and I, the Scottish are more friendly. I will leave it at that.</p>
									   <p>During my time in Scotland, I was able to see all the major monuments, museums and parks. The town is spread out, but everything is central to the train station because it sits directly in the middle; Everything is a short walk in relation to it. Calton Hill was no exception. It held a majority of the oldest monuments in Edinburgh and provided a 360-degree view of the town along with a large portion of the North Sea, which I must say was pretty incredible. Other places included the Christmas Market, Edinburgh Castle, National Scottish Museum, the National Galleries and Holyrood Park. I can’t say I had a bad experience with any of these places, but it is significant to understand how expensive a trip to the castle will set you back. $25 per person to be exact. In addition, the National Galleries were spread out across the town so for instance the main gallery is next to the train station but the portrait gallery is about a 10-minute walk away. I would highly recommend the National Scottish Museum as it is put together extremely well and the architecture mix is quite interesting. Plus it has a huge range of objects, new and (extremely) old. It could be compared to the Natural History Museum in New York, but it [the New York museum] would be put to shame.</p>
									   <p>Of course Scotland is known for its extremely beautiful land, especially in the Highlands, and so I had to visit the best viewing spot in Edinburgh, Holyrood Park. This is where I spent a majority of my time as I visited it twice so I could take it all in. Mudded, grassy and stone paths at high angles and elevations curved along the edge of the land to lead me near the peak. After taking a few breathers on the way, I finally reached the top and the view was indescribable, pictures have a hard time capturing it as well. There is something about the struggled journey to the top - the coldness in the air and the extreme wind across your uncovered body parts - and then finally seeing the land and sea while standing on the peak. The peak gives the best possible view as you are able to see other mountains in the far off distance among the pasture, city, and sea.</p>
									   <p>At the end of my trip I found myself wanting to see more of the landscape through hiking and so I would definitely make a return trip to visit the Highlands.</p>
									   <p>Observations: The sound of bagpipes, live or recorded, draws tourists like McDonalds attracts obese children. The Bank of Scotland has interesting money designs, distinctly different than English pounds.</p>
