	            	<h2 class="alpha">Stranded In London</h2>
	                   <p class="blog-post-meta">December 18, 2014 by <a href="/">Tyler</a></p>
					   <p>I found myself stranded in London or so it seemed.</p>
					   <p>To set the context of the situation, I had planned several weeks ahead my trip to Paris Gare du Nord from London. St. Pancras Station. I booked a hostel near the station through Expedia and reserved a spot with my American Express card. Since I was able to use my AMEX card through Expedia I assumed the place would just charge the same card. When I got to the hostel the desk clerk asked for payment and I was like has this room not already been paid for? He pointed out in very small print that the final price was to be paid upon arrival in cash or by Visa. Of course, I had a Visa debit card but for security reasons I only kept the necessary amount of money on the card in case it was pick-pocketed. I had no money in my Visa account at that time because the money wasn’t suppose to transfer until tomorrow, when I was in France.</p>
					   <p>After discovering this, I had to find an ATM that would accept AMEX cards. I tried the ATM at the hostel and no luck. It hit me there might be a money exchange service at St. Pancras so I tried that and the ATMs I found inside as well. Still no luck. I started to go into panic mode as it was nearly 7:00PM, I was alone and obviously had no other money.</p>
					   <p>I sat inside the terminal and started to think about what my next options were. Luckily I had my backpack with a laptop and cell phone with only a couple minutes remaining. That gave me an idea. I decided to go to the international section of St. Pancras station and sure enough there was an ATM that accepted American Express cards so I tried it. The machine kept asking for a PIN number and I had no idea what that was as I had never had to use this card for cash withdraw. I was able to use my cell phone and so I called the customer support line on the back of my card. From there I spoke with two separate employees and was finally able to setup a PIN and withdraw cash.</p>
					   <p>Later that night after I had taken money out and checked into the hostel I went for some cheap food, McDonalds. There was a homeless man with an empty Costa Coffee cup inside who I sat near. He was watching another homeless man through the window receive all kinds of food as they walked out of McDonalds. I could only assume he was asking the question why everyone was giving the other guy food and not him. So after finishing my meal, the homeless man inside still had nothing. I reached into my pocket and felt a few coins from when I had ordered. It wasn’t more than £4 or £5, but I gave it to him.</p>
					   <p>It was an interesting way to end the day as I had dealt with money problems that day myself, but it put things into perspective as this guy had no idea what or when his next meal would be.</p>
					   <p>Lessons learned: Always keep minutes on your phone when abroad. You might need them in an unexpected or unforeseen way. Know what your card customer support numbers are, especially international numbers. Always read the fine print on hostel bookings.</p>
