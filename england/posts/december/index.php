	            	<h2 class="alpha">Mail Abroad</h2>
	                   <p class="blog-post-meta">December 13, 2014 by <a href="/">Tyler</a></p>
					   <p>I haven’t written much if at all, about the mail services in Europe. It could be useful as everyone wants to send stuff back home, especially when traveling to places such as Europe. My experience with the Royal Mail, the mail provider for the UK, was not great. I found the staff was unable to communicate effectively and at times were often rude. The service was not great either. 3 cards I had sent home never reached the senders and a medium parcel I had also sent was delivered soggy and with a sizable hole in the side. Needless to say, I would think twice, maybe three times before I mailed something through the Royal Mail agin.</p>
					   <p>On the other hand, postcards mailed from France and Hungary never reached their final destination either or at least not yet. I can’t say much about these services other than the clerks speak English. The best experience I had with mail abroad was in Spain. Stamps and mailboxes were easy to find, the postcards shipped fast and they were actually received.</p>
					   <p>Overall, if you are traveling in Europe, you may want to skip postcard sending as it becomes more of a money trap than it is actually worth. You could just as easily get whomever a small gift for the same price as a stamp and postcard.</p>
