                    <p class="blog-post-meta">June 21, 2014 by <a href="/">Tyler</a></p>
                    <p>All study abroad students at a University of Missouri school has to enroll in HTH Worldwide health insurance. You only pay for the months you are not in the United States. For me that will be four months so my total was $129. We also had to sign up for Frontier MEDEX which gives basic help in times of need such as a translator, help with money, if a natural disaster occurs, mandatory evacuations, etc.. This service is no cost to the student and mandated through the university.</p>
