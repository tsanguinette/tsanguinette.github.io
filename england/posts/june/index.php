      			<h2 class="alpha">CAS Document & Visa Types</h2>
                    <p class="blog-post-meta">June 22, 2014 by <a href="/">Tyler</a></p>
                    <p>When entering the UK to study there are two types of visas to consider, a Student Visitor Visa and Tier4. The main difference between these documents is that a Student Visitor does not allow you to work or volunteer and you can only be in the country for six months. A Tier4 Visa allows you to work or volunteer plus you can stay in the country for a longer period of time. In order to get a Tier4 Visa you have to notify your study abroad university so they can issue a CAS document. This document basically states you are a student and declares your length of education. A Student Visitor Visa has no cost and can be obtained at port of entry, however, a Tier4 Visa has to be obtained before you enter the country and costs nearly $600.</p>
