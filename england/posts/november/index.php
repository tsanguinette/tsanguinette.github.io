	            	<h2 class="alpha">Budapest, Hungary</h2>
	                   <p class="blog-post-meta">November 13, 2014 by <a href="/">Tyler</a></p>
					   <p><img class="img-responsive" src="../../images/budapest.jpg"></p>
					   <p>To get to the airport, I took a train from Norwich to Ely and then from Ely to Stansted Airport. The train arrived at the airport about an hour and a half before my flight so it worked out well. The flight was around two hours and somewhat interesting. Apparently there were a few English guys at a bachelor party for a guy who had never flown before and was turned down in 7 different proposal attempts. I know because the flight attendants let them give a speech over the cabin microphone. Needless to say, It was one of those flights. After landing, passport control took a short time to go through. Once through, I checked in at the Minibus desk. I waited about 25 minutes before my bus came and the ride to Budapest took about 35-40.</p>
					   <p>The hotel, yes a hotel this time, Novotel is where I stayed. It was the only hotel directly across the Hungarian Parliament building on the Buda side. My room was on the seventh floor and had a partial view of the river. I didn't expect the hotel to be as nice as it was. It was great to have a big bed, a couch, and a TV because you definitely don't get any of that in UEA accommodation. On the first night, Thursday, I got my hotel, checked in and then ate dinner at the hotel restaurant. It was probably the best meal I have had since being in Europe. I had roast chicken breast with grilled vegetables. Basic but SO good! Later, after dinner, I went for a walk down the street to the local square where there was a grocery store. I bought a few things to snack on at the hotel and on my way back I took a few pictures of the parliament buildings with all of its lights.</p>
					   <p>On Saturday, I started out by going to St. Stephen's Basilica. It opened at 9:00 am and I was basically the first person in. The walk from my hotel was only about 15 minutes and I crossed the Chain Bridge, the oldest bridge to connect Buda and Pest, among other interesting places. When I got to the basilica there was no one in line at all. I found that kind of weird especially for a Friday morning, although, it was nice to have the entire building to myself for a few minutes. After finishing up, I headed toward the parliament building. On my way, I went through Szabadság tér which had the Szovjet háborús emlëkmü (Soviet War Memorial). I also found the Ronald Reagan statue walking toward the U.S. Embassy in the park. Once I reached the parliament building I waited about 20 minutes outside because I couldn't go through security to get inside until 10:00 am. Security wasn't that strict as they didn't check any kind of legal documents before entering, just a simple airport-like screening. To get from the visitors center underneath the building to the main building level, we had to climb about 120 or so steps. The tour consisted of about three rooms: the main hall, the Crown Jewels and one of the voting rooms. It lasted for only 30 minutes and I don't think it was that bad for the price.</p>
					   <p>Next, I headed back across the river to the Buda Castle. This is where the National Gallery and History museum is. I would have to say this should be your top visit in Budapest if you visit. The is situated on a hill so the front side overlooks Buda, the river, and Pest while the back side overlooks the Buda Mountains. There is basically a fantastic view everywhere you walk at the castle level. The National Gallery costs 1400 HUF which is about $5.75. Be sure to walk all the way around the building because there are so many cool small things going on as well like the changing of the guards. When finished with the gallery, I grabbed lunch - It only cost 750 HUF. To take a break from essentially a day worth of walking I sat on the Buda mountain side and people watched for awhile. You can always tell who the Americans are because they are usually the loudest and look the most lost. To end the day, I walked back to the hotel but on my way I stopped at the grocery store again. When in Budapest, try food especially if you have been in England for a long time. Anything you eat in Budapest will be exponentially better than in England.</p>
					   <p>Finally, on Saturday, I went to Mammut. Mammut is basically a huge inside mall via a 10-minute walk from the hotel. My main reason for going was to mail some postcards. Posta's are open weird hours here so I had to go to a bigger one and mail things on a Saturday. A stamp to the U.S. cost 380 HUF. For my last few hours in Budapest, I walked to the Matthias Basilica. It cost 1200 HUF to get in. The church is actually pretty cool and worth the money, but it's such a tragedy from an art historical perspective. There is a section in the upper museum about restorations to the art pieces and the touching up to the paintings are so distorting. The facial features, proportions, and shadows are nowhere near the original which was actually quite surprising. When it was time to head to the airport I checked out of my hotel and took the Minibus.</p>
					   <p>The Budapest airport is actually setup pretty well. Once you go through security, just down from the ticket gate, you go to the main atrium which has plenty of comfortable seating overlooking the tarmac. The top level has a decent set of restaurants and free wi-fi.</p>
					   <p>The train back to Norwich consisted of a transfer at Cambridge only taking a total time of about an hour and a half from the airport.</p>
					   <p>Notes about Budapest: From reading internet articles of how nobody speaks English, in my experience everything was fine. I had no problems communicating in most areas you'll go have English speaking employees. I imagined the girls would not be attractive, something of German looks, but I was wrong. Many look like American girls, but they are obviously more modest here. Post office boxes are red rectangles with a sloped roof, usually hung on a wall and tagged with stickers/graffiti. Euros aren't accepted in a local context, only at major places like the airport so bring plenty of forint. Items are usually pretty cheap, for the most part. Take the minibus to and from the airport, don't try to wing public transportation. It only cost about $20 round trip.</p>
