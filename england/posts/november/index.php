	            	<h2 class="alpha">Food</h2>
	                   <p class="blog-post-meta">November 09, 2014 by <a href="/">Tyler</a></p>
					   <p>The English try so hard to imitate the “American” burger but can’t even come close. I recently went to Ed’s Easy Diner, a traditional “American” diner, at Chapelfield Mall. So many things weren’t authentic. The guys uniform consisted of a v-neck shirt and black pants. I’ll state the obvious, but I’m pretty sure v-neck cuts were mainly worn my women in the 50’s and 60’s, no bowties either. When ordering, crisps were changed to fries but the “fries” were in no way similar to American diners. They weren’t bad, but they just didn’t fit the restaurant. Finally, when you order a cheeseburger it basically means you want everything and by everything I mean pickles, onions, lettuce, tomato, cheese and a bunch of sauce.</p>
					   <p>Other food such as local fresh baked goods from Tesco, Sainsbury or shops are usually pretty good, way better and cheaper than anything in the U.S. The fruit is normally good although recently my grapes from Tesco have come with mold so I just quit buying them. Pizza is decent, for the most part. Norwich has a Pizza Hut, Dominos delivers to campus and frozen pizzas aren’t too bad. Thin crust here isn’t the same as St. Louis thin crust pizza though, especially the thin and crispy part though. Fish and chips. I don’t think I need to say anymore about that. American chips are called crisps here and there are no larger bags like in America. Here you will get a larger bag filled with smaller, individual, bags.</p>
