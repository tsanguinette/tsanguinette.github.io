	            	<h2 class="alpha">Paris, France</h2>
	                   <p class="blog-post-meta">November 26, 2014 by <a href="/">Tyler</a></p>
					   <p><img class="img-responsive" src="../../images/paris.jpg"></p>
					   <p>I recently went to Paris for a decent amount of time. With the semester coming to an end, I am a bit tired of writing in general so I’m not going to give a detailed description of my trip to Paris. I’ll just list a few things that I liked and disliked and give some quick advice.</p>
					   <p>Liked:</p>
					   <ul>
						   <li>The metro will take you everywhere once you get the hang of it so buy a multiple day pass.</li>
						   <li>The city is beautiful in small doses but the countryside is even better.</li>
						   <li>Buy a Paris Museum Pass or purchase tickets in advance!! I bypassed incredibly long lines at every place this way.</li>
						   <li>Pastries are so good and usually pretty cheap if you buy from a local bakery.</li>
						   <li>It was easy to communicate, for the most part.</li>
					   </ul>
					   
					   <p>Disliked:</p>
					   <ul>
						   <li>Paris can be a tourist trap.</li>
						   <li>Way too many aggressive street vendors.</li>
						   <li>Everything was extremely expensive.</li>
						   <li>The French Fries were terrible.</li>
						   <li>Local grocery stores were somewhat hard to find.</li>
						   <li>Going out by yourself can be sketchy at night.</li>
						   <li>The graphic design and wayfinding signs around the city were very poor. Incredibly lacking and confusing compared to other European cities.</li>
					   </ul>
					   
					   <p>Advice: Get going early. Most tourists don’t hit the town until about 10:00am so anything before that is golden, especially parks and other public areas. You’ll have fewer tourist photobombs and be able to enjoy the area a lot more. The Paris Museum Pass is a must along with a multiple day metro pass as described above. Exchange plenty of money because this city is EXPENSIVE. Use your credit/debit card when possible if you don’t have to pay foreign transaction fees. If you are going to carry a bag, pack light because you’ll walk a lot and go through security many times. Plan your day ahead.</p>
					   
					   <p>Photos at <a href="https://www.flickr.com/photos/tsanguinette/">https://www.flickr.com/photos/tsanguinette/</a></p>
