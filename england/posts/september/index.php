			            	<h2 class="alpha">British Culture</h2>
			                   <p class="blog-post-meta">September 29, 2014 by <a href="/">Tyler</a></p>
							   <p>“You okay?”, a common phrase used as a greeting by British natives. It basically means the same in America as “How are you doing?” which it is acceptable to say “Fine, thanks, you?”. After you hear it a few times it comes natural but for the first week or so it feels weird being asked that.</p>
							   <p>You know how people always ask “If you could change one thing in the world what would it be?”. After living in England, I would have to say the way people walk on the sidewalk here. It is literally anarchy. Do you walk on the right, the left, in the middle or both??? The answer is all of the above. People basically walk in every direction, right or not. Most of the time, it ends in an awkward dance of trying to read the other person's actions and being polite.</p>
							   <p>English people say, “Cheers” instead of goodbyes. So when you are purchasing an item say at the store the cashier will most likely say “Cheers" instead of “Thanks, have a good day”.</p>
							   <p>There is a coin or card for everything. My pockets are usually weighted down with £1 and £2 coins. On top of the basic pound coins, you get denominations in 10 all the way to 1p. Needless to say, you end up with tons of change.</p>
							   <p>It is customary to thank the bus driver when you exit the bus whether it be a private coach or public transportation. Also, it is socially acceptable to sit on the second level as a first choice or if you prefer the bottom, sit at least two or three rows from the stairs. This leaves room for older people to have the closest seats.</p>
							   <p>Partying usually goes on into the next day, especially on Friday night. For instance, during week 2 of Freshers events a Prom took place. This consisted of DJs and a carnival. Most of the partying ended around 3-5am. If you like quite time, Sunday morning is great. Everyone is usually hungover and don’t get out of bed until later in the day.</p>
							   <p>Royal Mail takes forever. It literally puts the snail in mail.</p>
