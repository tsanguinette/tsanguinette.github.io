	            	<h2 class="alpha">Travel Experience</h2>
	                  <p class="blog-post-meta">September 16, 2014 by <a href="/">Tyler</a></p>
	                  <p>The travel to England was very draining. I had three flights and every single one of them were delayed. Thankfully I made all my connecting flights with no rebooking. Sometimes you see people running from flight to flight, that was me in Atlanta. I ran from terminal C55 to the airport train and then to the international terminal. Everyone had boarded the plane, but I made it about 10 minutes before they closed the door.</p>
					  <p>The flight to Amsterdam was also an interesting experience as well. Delta’s 767-300 planes are massive. Its in-flight entertainment system was a nice touch although the old lady sitting next to me kept asking for help. The system had a touch screen built into the seat in front of you with tons of movies, games, TV shows and even a flight tracker where you could see a map of your flight path and plane details. As part of international flights Delta serves dinner (usually) and breakfast along with any other drink/snacks you want. For dinner, I had a grilled chicken salad with fruit, roll, and cookie. It was actually pretty good and filling for an in-flight meal, however, the breakfast was disgusting. It consisted of a lukewarm “egg sandwich” aka a severely disgusting roll with a sponge-like patty of egg along with yogurt. Sleeping was a bit iffy as I didn’t get much sleep, oh and not to mention the bratty child who kept having outbursts of high-pitched yells kept most passengers up.</p>
					  <p>Schiphol airport was interesting because of its size and complexity, although it can be a bit confusing as far as security screening and gate access. Perhaps it is not as confusing for larger flights but I took a KLM city hopper to Norwich so the plane was smaller and had a remote boarding location. To board the plane passengers waited at a gate that is really a bus stop. Once your ticket is scanned you board a bus which then transports you to the tarmac. From the gate to the plane it takes about 5 minutes. You literally walk on the plane from the tarmac. It was surrounded by other smaller planes much like sailboats sit in the wave break zone of lake Michigan in Chicago. The extremely short flight of 30 minutes was okay. KLM served a prepackaged cup of water less than a couple ounces along with a biscuit which was great. It tasted like snickerdoodle cookies! Oh, for your information, there is a wind farm in the middle of the sea between the Netherlands and England. It was kind of unexpected surrounded by ships and water.</p>
					  <p>After arriving in Norwich, I made it through passport control. I was asked some rather awkward questions but nonetheless they stamped my passport with a student visitor seal.</p>
