	            	<h2 class="alpha">Day 2</h2>
	                  <p class="blog-post-meta">September 18, 2014 by <a href="/">Tyler</a></p>
					  <p><img class="img-responsive" src="../../images/lake.jpg"></p>
	                  <p>Day 2 was pretty nice. I started out with a coffee (latte £1.80) and a cup of fruit (£2.50) from Café Direct, the main coffee shop next to the store. We met at 9:30 in the campus center for a campus tour. On the tour, I meet a small group of people which I ended up having lunch with and then eventually dinner as well. They were from Germany, Slovenia, Canada and some middle eastern country. It was hard to understand the middle eastern guy. After the tour, we grabbed lunch and talked for a while. Meeting friends on tours can be awkward especially if you are an international students. Just look at the person next to you and ask them their name. It will eventually lead to a familiar face or lunch/dinner date.</p>
					  <p>In the time, I had between tours I went for a walk around the campus’s lake. It was classic countryside territory and definitely a beautiful place to see.</p>
					  <p>Later in the afternoon three of us ended up on the same bus for the Norwich city tour. The city tour took about an hour and a half. We had dinner on campus at the Campus Kitchen where I had a burger and fries. Needless to say, it wasn’t even close to American burgers. Finally towards the end of the evening it was late so we all turned in and went back to our rooms.</p>
					  <p>There is a bunch of people from the U.S. here this semester. I’ve met people from Massachusetts, California, Texas and even Missouri — about five people are from St. Louis. Of course, I have also met tons of people from other countries. Some include Kenya, Mongolia, Brasil, Japan, Canada, Germany, and Slovenia.</p>
					  <p>Upon returning from dinner, I purchased groceries from ASDA. The first couple days can be a bit rough food wise so you will definitely want to order groceries online and have them delivered to your flat once you get connected to the internet. Note that the campus mail room doesn’t accept perishable items so do include your flat name on your account. You will also need a local phone number so the best way to take care of this is by using the GiffGaff sim card you receive in your room on arrival day. If you are outside of the main campus you can order a free sim from the GiffGaff website. The service is cheap and will easy to setup.</p>
					  <p>Side note: English people love to queue (wait in line). You will queue for everything.</p>
