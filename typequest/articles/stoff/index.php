				<p><span class="sans">What is one thing you would like other web and graphic designers to know aboutÂ web fonts?</span> The basics of typography don&#8217;t go out the window when you&#8217;re working on the web &mdash; the web is largely interpreted through reading, so design it as such. Keep readability in mind first, design around that. Use contrasting typefaces when appropriate to draw attention where you want it.
</p>
				<p><span class="sans">How are webfonts contributing to brand development on multi media devices?</span> Brands have always used typography choices (along with photography choices, layout choices, etc) in various media to create a signature visual style that they can own &mdash; it&#8217;s an essential part of branding. The advent of truly cross-platform webfonts has brought that aspect of design to the web, since it&#8217;d previously had a limited type palette to work with. Some companies &mdash; take the New York Times as an example &mdash; will even commission web versions of their print faces to closely tie their media presence together.</p>
				<p><span class="sans">What makes a great webfont worth purchasing?</span> Firstly, a great webfont is optimized for the screen &mdash; check out HF&J&#8217;s painstakingly detailed work to bring the character of their classic typefaces to the screen, and you quickly realize how pixels are different than paper. Secondly, in my opinion, a good webfont will include a ton of characters, including ligatures. Thirdly, purchase a webfont if it&#8217;s a client job &mdash; they&#8217;re the ones paying, after all.</p>
